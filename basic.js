let apple=23
console.log(apple)
console.log(typeof(apple))
//Let keyword reassign the value
apple="fruites"
console.log(apple)
console.log(typeof(apple))

//var keyword we can redeclare and reassign the value
var banana=2
console.log(banana)
console.log(typeof(banana))
var banana="yellow"
console.log(banana)
console.log(typeof(banana))
var banana=25.20
console.log(banana)
console.log(typeof(banana))

//not allow redeclare and reassign the value
const tree=2
console.log(tree)
console.log(typeof(tree))

